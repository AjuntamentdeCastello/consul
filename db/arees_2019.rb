areas.each do |area|
  current_area = Area.where(name: [area[:name][:val], area[:name][:es]]).first
  if current_area.nil?
    current_area = Area.new(name: area[:name][:es])
    current_area.save
  end
  area_translation_val = Area::Translation.find_or_create_by(area_id: current_area.id, locale: 'val')
  area_translation_val.name = area[:name][:val]
  area_translation_val.save
  area[:subareas].each do |subarea|
    current_subarea = SubArea.where(name: [subarea[:val], subarea[:es]]).first
    if current_subarea.nil?
      current_subarea = SubArea.new(name: subarea[:es], area_id: current_area.id)
      current_subarea.save
    end
    subarea_translation_val = SubArea::Translation.find_or_create_by(sub_area_id: current_subarea.id, locale: 'val')
    subarea_translation_val.name = subarea[:val]
    subarea_translation_val.save
  end
end
